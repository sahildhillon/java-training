package assignment;

public class MyOwnClass2 {
	private int age;
	private String name;
	

	public MyOwnClass2(int roll, String name) {
		super();
		this.age = roll;
		this.name = name;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}

	
	
}
